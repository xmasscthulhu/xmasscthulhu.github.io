
//num is the number of dice being rolled, d is highest value of the die being rolled (4 for d4, 6, for d6, etc.)
function dice(num,d) {
    roll = 0;
    for (i = 0; i < num; i++) {
        roll += randomDieRoll(d,1);
    }
    return roll;
}

function randomDieRoll(max,min) {
    return Math.floor(Math.random() * (max - min + 1)) + min;
}

